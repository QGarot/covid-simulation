def info(text=""):
    """
    Affiche une information
    :param text:
    """
    print("[INFO] " + text)


def error(text=""):
    """
    Affiche une erreur
    :param text:
    """
    print("[ERROR] " + text)


def line(text=""):
    """
    Affiche une ligne sans tag
    :param text:
    """
    print(text)
